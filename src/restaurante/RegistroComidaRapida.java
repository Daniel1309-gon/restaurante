    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            Connection con = DriverManager.getConnection(url, user, pass);
            PreparedStatement pst = con.prepareStatement("INSERT INTO menurapido VALUES (?,?,?,?,?)");
            
            pst.setString(1, txtNombreComida.getText().trim());
            pst.setDouble(2, Double.parseDouble(txtPrecioComida.getText().trim()));
            pst.setString(3, txtTipoComida.getText().trim());
            pst.setString(4, txtSalsasComida.getText().trim());
            pst.setBoolean(5, Boolean.parseBoolean(txtComboComida.getText().trim()));
            pst.executeUpdate();
            
            txtNombreComida.setText("");
            txtPrecioComida.setText("");
            txtTipoComida.setText("");
            txtSalsasComida.setText("");
            txtComboComida.setText("");
        } catch (SQLException e) {
            System.out.println(e);
        }
        JOptionPane.showMessageDialog(null, "Registro exitoso");
    }//GEN-LAST:event_jButton1ActionPerformed

