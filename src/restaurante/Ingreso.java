    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            Connection con = DriverManager.getConnection(url, user, pass);
            PreparedStatement pst = con.prepareStatement("SELECT * FROM empleados WHERE Nombre = ?");

            pst.setString(1, usuario.getText().trim());
            ResultSet result = pst.executeQuery();
            while (result.next()) {
                usuarioSISTEMA = result.getString("Nombre");
                clave = result.getString("Password").toCharArray();
                
            }
            usuarioIngresado = usuario.getText().trim();
            char [] passwordIngresado = jPasswordField1.getPassword();

            if (usuarioIngresado.equals(usuarioSISTEMA) && Arrays.equals(passwordIngresado, clave)) {
                PrincipalEmpleado principalEmpleado = new PrincipalEmpleado();
                principalEmpleado.setVisible(true);
                this.setVisible(false);
            } else {
                JOptionPane.showMessageDialog(null, "Datos incorrectos, vuelve a intentar");
            }
        } catch (SQLException e) {
        }
    }//GEN-LAST:event_jButton1ActionPerformed

